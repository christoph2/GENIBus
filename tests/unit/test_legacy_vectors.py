def test_apdu_set_commands_vector() -> None:
    assert _to_hex(
        apdu.create_set_commands_pdu(
            apdu.Header(defs.FrameType.SD_DATA_REQUEST, 0x20, 0x01),
            ["REMOTE", "START"],
        )
    ) == [0x27, 0x06, 0x20, 0x01, 0x03, 0x82, 0x07, 0x06, 0x07, 0xFA]


def test_apdu_set_values_vector() -> None:
    assert _to_hex(
        apdu.create_set_values_pdu(
            apdu.Header(defs.FrameType.SD_DATA_REQUEST, 0x20, 0x01),
            references=[("ref_rem", 0xA5)],
        )
    ) == [0x27, 0x06, 0x20, 0x01, 0x05, 0x82, 0x01, 0xA5, 0x0F, 0x4C]


def test_apdu_get_info_vector() -> None:
    assert _to_hex(
        apdu.create_get_info_pdu(
            klass=defs.APDUClass.MEASURED_DATA,
            header=apdu.Header(defs.FrameType.SD_DATA_REQUEST, 0x20, 0x01),
            measurements=["h", "q", "p", "t_w", "speed_hi", "energy_hi"],
        )
    ) == [0x27, 0x0A, 0x20, 0x01, 0x02, 0xC6, 0x25, 0x27, 0x22, 0x3A, 0x23, 0x98, 0x33, 0x5A]

